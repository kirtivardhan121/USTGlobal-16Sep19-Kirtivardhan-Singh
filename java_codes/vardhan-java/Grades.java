class Grades{
	public static void main(String args[]){
	
	char grades = 'A';
	
	if( grades == 'A' )
	{
		System.out.println("FCD");
	}
	else if( grades == 'B' )
	{
		System.out.println("FC");
	}
	else if( grades == 'C' )
	{
		System.out.println("SC");
	}
	else if( grades == 'D' )
	{
		System.out.println("Pass");
	}
	else if( grades == 'E' )
	{
		System.out.println("Fail");
	}else{
		System.out.println("Invalid Grades");
		}

	}
}